# 6층까지 각각 12칸씩 10번째면?

# 1 7
# 2 8
# 3 9
# 4 10
# 5 11
# 6 12

for __ in range(int(input())):
    _ = list(map(int, input().split(" ")))
    if _[0] >= _[2]:
        print(f"{_[2]}01")
    else:
        if _[2] % _[0] == 0:
            print(f"{_[0]}{_[2]//_[0]:02}")
        else:
            print(f"{_[2]%_[0]}{_[2]//_[0] + 1:02}")